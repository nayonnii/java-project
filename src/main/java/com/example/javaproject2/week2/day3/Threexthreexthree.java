package com.example.javaproject2.week2.day3;

import java.util.Arrays;

public class Threexthreexthree {
    public static void main(String[] args) {
        int[][][] arr3x3x3 = new int[3][3][3];  //3차원배열 선언 및 초기화
        System.out.println(Arrays.toString(arr3x3x3[0][0]));
        System.out.println(Arrays.toString(arr3x3x3[0][1]));
        System.out.println(Arrays.toString(arr3x3x3[0][2]));
        System.out.println(Arrays.toString(arr3x3x3[1][0]));
        System.out.println(Arrays.toString(arr3x3x3[1][1]));
        System.out.println(Arrays.toString(arr3x3x3[1][2]));
        System.out.println(Arrays.toString(arr3x3x3[2][0]));
        System.out.println(Arrays.toString(arr3x3x3[2][1]));
        System.out.println(Arrays.toString(arr3x3x3[2][2]));
    }
}
